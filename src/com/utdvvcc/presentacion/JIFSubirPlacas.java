    private void btn_subirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_subirActionPerformed
        try
            
        {
            
          int opc=JOptionPane.showConfirmDialog(null,"Desea subir las placas a la base de datos?", "Aviso!", JOptionPane.YES_NO_OPTION); 

              if(opc==JOptionPane.YES_OPTION)
              
              {
    
           if(tb_placas.getRowCount()== 0 && tb_placas.getSelectedRow()==-1)
        {
         
           throw new Exception("La Tabla no tiene ning√∫n item adicionado!");

        } 
                  
                  
            String form="", placa="";
            
            
            
           
            idusuario=MDIPrincipal.getvSeccion();
            
              for(int i=0;i<tb_placas.getRowCount();i++)
              {
                  form=(String)tb_placas.getValueAt(i,0);
                  placa=(String)tb_placas.getValueAt(i,1);
                  
                  if(form.equals("") || placa.equals(""))
                  {
                    throw new Exception("La columna Form y/o Placa estan vacias");

                  }
                  
                  conexion.executeUpdateQuery("INSERT INTO tb_tarjetas_especiales (placa, id_usuario,form)VALUES('"+placa+"',"+idusuario+", '"+form+"')");
                  conexion.desconectar();
              }
              JOptionPane.showMessageDialog(null,"Se registraron un total de: " + tb_placas.getRowCount()+ " Placas");
              
              //conexion.desconectar();
              
           }
            
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e, "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btn_subirActionPerformed


