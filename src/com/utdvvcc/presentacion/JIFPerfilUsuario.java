    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        try {
            
            
            if(txt_marca.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "El campo nombre de perfil es obligatorio");
            }
            else
            {
            
            
            String nom=txt_marca.getText().toUpperCase();
            String des=txa_des.getText();
            pe=new Perfil();
            daoper=new DaoPerfil();
            
            pe.setNomperfil(nom);
            pe.setDescripcion(des);
            daoper.guardar(pe);
            JOptionPane.showMessageDialog(this, "Se guardo con exito el perfil de usuario.");
            LimpiarTabla();
            consultarTodos();
            JD_guardar.dispose();
          }
            
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
        
    }//GEN-LAST:event_btn_guardarActionPerformed

